<h1>VARIABLES (Booléen)</h1>

<?php
  // TYPE BOOLEAN

  // Type booléen (true or false)
  $isHobbit = true;

  if ($isHobbit === true) {
    echo "Frodon est hobbit";
  } else {
    echo "Legolas n'est pas un hobbit";
  }

  /*
      if ($isHobbit === true)
      est la même chose que
      if ($isHobbit)
  */

  /*
      "==" : test sur la valeur
      "===" : test sur la valeur et le type
  */
?>